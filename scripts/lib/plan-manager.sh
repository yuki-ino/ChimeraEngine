    local task_id="$1"
    
    if grep -q "^| ${task_id} |" "${PLAN_FILE}"; then
        if grep -A 100 "### å®Ÿè¡Œä¸­ã®ã‚¿ã‚¹ã‚¯" "${PLAN_FILE}" | grep -B 100 "^###" | grep -q "^| ${task_id} |"; then
            echo "active"
        elif grep -A 100 "### å¾…æ©Ÿä¸­ã®ã‚¿ã‚¹ã‚¯" "${PLAN_FILE}" | grep -B 100 "^###" | grep -q "^| ${task_id} |"; then
            echo "waiting"
